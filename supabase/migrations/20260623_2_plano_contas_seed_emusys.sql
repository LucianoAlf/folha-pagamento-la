update public.plano_contas c set parent_id = p.id
from public.plano_contas p
where c.parent_id is null and c.codigo like '%.%'
  and p.codigo = regexp_replace(c.codigo, '[.][^.]+$', '');

insert into public.centros_custo (codigo, nome, tipo, ordem)
select x.codigo, x.nome, x.tipo, x.ordem
from (values ('cg','Campo Grande','unidade',1),('rec','Recreio','unidade',2),('bar','Barra','unidade',3))
  as x(codigo, nome, tipo, ordem)
where not exists (select 1 from public.centros_custo cc where cc.codigo = x.codigo);
